/**
 * Get country name by numeric id or ISO-3 alpha code.
 * @param {string} id - Numeric code (e.g. "643") or ISO-3 (e.g. "RUS")
 * @returns {string}
 */
export function getCountryName(id) {
  if (!id) return 'Неизвестная';

  // Try numeric first
  const numId = String(id).padStart(3, '0');
  if (COUNTRY_NAMES[numId]) return COUNTRY_NAMES[numId];

  // Try ISO-3 -> numeric
  if (ISO3_TO_NUM[id]) return COUNTRY_NAMES[ISO3_TO_NUM[id]] || id;

  return id;
}

/**
 * Get ISO-3 code from numeric code.
 */
export function numericToIso3(numCode) {
  const num = String(numCode).padStart(3, '0');
  for (const [iso3, nCode] of Object.entries(ISO3_TO_NUM)) {
    if (nCode === num) return iso3;
  }
  return `T${num}`;
}
